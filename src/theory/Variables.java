package theory;

public class Variables {
    public static void main(String[] args) {
        /* Java is a typed language!
         *
         * Types: 
         * 1 - int (values/numbers)
         * 2 - char (characters)
         * 3 - boolean (true/faLse)
         * 4 - double (decimal values)
         * 
         * Construction:
         * <type> <nameOfVariable> = <valueAssignment>;
         */
        int i = 10;
        double b = 12.50;
        char grade = 'A';
        boolean boo = true;

        System.out.println("I have the number " + i);
        System.out.println("I have dollars $" + b);
        System.out.println("My grade was " + grade + "+");
        System.out.println("My god... Is this " + boo + "");
    }
}
